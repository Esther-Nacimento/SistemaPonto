    public static async Task SincronizarVinculosInstitucionais(string connectionString)
    {
        await using var con = await Abrir(connectionString);
        await using var cmd = new NpgsqlCommand("""
            INSERT INTO app_supervisores (usuario_id, orgao_id)
            SELECT id, orgao_id
            FROM app_usuarios
            WHERE tipo_usuario = 'SUPERVISOR'
            ON CONFLICT (usuario_id) DO UPDATE SET orgao_id = EXCLUDED.orgao_id;

            UPDATE app_academicos a
            SET supervisor_id = s.usuario_id
            FROM app_supervisores s
            WHERE a.supervisor_id IS NULL
              AND a.orgao_id IS NOT NULL
              AND s.orgao_id = a.orgao_id;
            """, con);

        await cmd.ExecuteNonQueryAsync();
    }

    public static async Task<int> InserirUsuario(NpgsqlConnection con, NpgsqlTransaction tx, CadastroUsuarioRequest dados, string cpf, string tipo)
    {
        await using var cmd = new NpgsqlCommand("""
            WITH orgao_upsert AS (
                INSERT INTO app_orgaos (nome)
                VALUES (@orgao)
                ON CONFLICT (nome) DO UPDATE SET nome = EXCLUDED.nome
                RETURNING id
            )
            INSERT INTO app_usuarios (nome_completo, cpf, data_nascimento, email, senha_hash, tipo_usuario, orgao, orgao_id)
            VALUES (@nome, @cpf, @dataNascimento, @email, @senhaHash, @tipo, @orgao, (SELECT id FROM orgao_upsert))
            ON CONFLICT (cpf) DO NOTHING
            RETURNING id
            """, con, tx);

        cmd.Parameters.AddWithValue("nome", dados.NomeCompleto.Trim());
        cmd.Parameters.AddWithValue("cpf", cpf);
        cmd.Parameters.AddWithValue("dataNascimento", (object?)dados.DataNascimento ?? DBNull.Value);
        cmd.Parameters.AddWithValue("email", dados.Email.Trim());
        cmd.Parameters.AddWithValue("senhaHash", Texto.HashSenha(dados.Senha));
        cmd.Parameters.AddWithValue("tipo", tipo);
        cmd.Parameters.AddWithValue("orgao", dados.Orgao.Trim());

        var id = await cmd.ExecuteScalarAsync();
        if (id == null)
            throw new InvalidOperationException("Ja existe um cadastro com este CPF.");

        return Convert.ToInt32(id);
    }

    public static async Task<object?> BuscarUsuarioResposta(string connectionString, int id, bool comPontoHoje = false)
    {
        await using var con = await Abrir(connectionString);
        await using var cmd = new NpgsqlCommand("""
            SELECT u.*, a.*,
                   p.id AS ponto_id, p.data AS ponto_data, p.entrada AS ponto_entrada,
                   p.saida AS ponto_saida, p.horas_cumpridas AS ponto_horas_cumpridas,
                   p.status AS ponto_status
            FROM app_usuarios u
            LEFT JOIN app_academicos a ON a.usuario_id = u.id
            LEFT JOIN app_pontos p ON p.academico_id = u.id AND p.data = CURRENT_DATE
            WHERE u.id = @id
            """, con);

        cmd.Parameters.AddWithValue("id", id);

        await using var reader = await cmd.ExecuteReaderAsync();
        if (!await reader.ReadAsync())
            return null;

        return comPontoHoje && Valor.TemColuna(reader, "ponto_id")
            ? Resposta.AcademicoSupervisor(reader)
            : Resposta.Usuario(reader);
    }

    public static async Task<UsuarioInterno?> BuscarUsuarioInterno(string connectionString, int id)
    {
        await using var con = await Abrir(connectionString);
        await using var cmd = new NpgsqlCommand("""
            SELECT id, nome_completo, cpf, email, tipo_usuario, orgao, orgao_id
            FROM app_usuarios
            WHERE id = @id
            """, con);

        cmd.Parameters.AddWithValue("id", id);
        await using var reader = await cmd.ExecuteReaderAsync();

        return await reader.ReadAsync()
            ? new UsuarioInterno(
                reader.GetInt32(0),
                reader.GetString(1),
                reader.GetString(2),
                reader.GetString(3),
                reader.GetString(4),
                reader.GetString(5),
                reader.IsDBNull(6) ? null : reader.GetInt32(6))
            : null;
    }

    public static async Task<PontoInterno?> BuscarPontoRegistro(NpgsqlConnection con, int academicoId, DateTime data)
    {
        await using var cmd = new NpgsqlCommand("""
            SELECT id, data, entrada, saida, horas_cumpridas, status
            FROM app_pontos
            WHERE academico_id = @academicoId AND data = @data
            """, con);

        cmd.Parameters.AddWithValue("academicoId", academicoId);
        cmd.Parameters.AddWithValue("data", data.Date);

        await using var reader = await cmd.ExecuteReaderAsync();
        if (!await reader.ReadAsync())
            return null;

        return new PontoInterno(
            reader.GetInt32(0),
            reader.GetDateTime(1),
            reader.IsDBNull(2) ? null : reader.GetDateTime(2),
            reader.IsDBNull(3) ? null : reader.GetDateTime(3),
            reader.GetDouble(4),
            reader.GetString(5));
    }

    public static async Task<object?> BuscarPonto(string connectionString, int academicoId, DateTime data)
