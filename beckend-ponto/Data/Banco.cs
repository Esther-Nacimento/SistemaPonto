        cmd.Parameters.AddWithValue("supervisorId", (object?)supervisorId ?? DBNull.Value);
        await cmd.ExecuteNonQueryAsync();
    }

    public static async Task<List<object>> HistoricoMensal(string connectionString, int academicoId, int ano, int mes)
    {
        await using var con = await Abrir(connectionString);
        await using var cmd = new NpgsqlCommand("""
            SELECT h.*, p.entrada AS ponto_entrada, p.saida AS ponto_saida,
                   p.horas_cumpridas, p.status AS ponto_status
            FROM app_historicos h
            LEFT JOIN app_pontos p ON p.academico_id = h.academico_id
                AND p.data = make_date(h.ano, h.mes, h.dia)
            WHERE h.academico_id = @academicoId AND h.ano = @ano AND h.mes = @mes
            """, con);

        cmd.Parameters.AddWithValue("academicoId", academicoId);
        cmd.Parameters.AddWithValue("ano", ano);
        cmd.Parameters.AddWithValue("mes", mes);

        var salvos = new Dictionary<int, object>();
        await using (var reader = await cmd.ExecuteReaderAsync())
        {
            while (await reader.ReadAsync())
                salvos[reader.GetInt32(reader.GetOrdinal("dia"))] = Resposta.Historico(reader);
        }

        var dias = new List<object>();
        for (var dia = 1; dia <= DateTime.DaysInMonth(ano, mes); dia++)
        {
            if (salvos.TryGetValue(dia, out var salvo))
            {
                dias.Add(salvo);
                continue;
            }

            var data = new DateTime(ano, mes, dia);
            var feriado = Feriado(data);
            dias.Add(new
            {
                id = 0,
                academicoId,
                ano,
                mes,
                dia,
                data,
                entrada = (DateTime?)null,
                saida = (DateTime?)null,
                horaEntrada = (DateTime?)null,
                horaSaida = (DateTime?)null,
                horasCumpridas = 0d,
                status = "AUSENTE",
                observacaoSupervisor = feriado.Descricao,
                tipoDia = feriado.TipoDia,
                descricaoFeriado = feriado.Descricao
            });
        }

        return dias;
    }

    public static (string TipoDia, string Descricao) Feriado(DateTime data)
    {
        var tipoDia = data.DayOfWeek switch
        {
            DayOfWeek.Saturday => "SABADO",
            DayOfWeek.Sunday => "DOMINGO",
            _ => "UTIL"
        };

        var feriados = new Dictionary<(int, int), string>
        {
            [(5, 1)] = "Dia do Trabalhador",
            [(6, 4)] = "Corpus Christi",
            [(9, 7)] = "Independencia do Brasil",
            [(10, 12)] = "Nossa Senhora Aparecida",
            [(11, 2)] = "Finados",
            [(11, 20)] = "Consciencia Negra",
            [(12, 25)] = "Natal"
        };

        return feriados.TryGetValue((data.Month, data.Day), out var descricao)
            ? ("FERIADO", descricao)
            : (tipoDia, string.Empty);
    }
