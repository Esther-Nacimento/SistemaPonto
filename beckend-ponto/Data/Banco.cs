    }

    public static async Task SincronizarHistorico(NpgsqlConnection con, int academicoId, DateTime data)
    {
        var feriado = Feriado(data);
        await using var cmd = new NpgsqlCommand("""
            INSERT INTO app_historicos (
                academico_id, ano, mes, dia, entrada, saida, observacao_supervisor,
                tipo_dia, descricao_feriado, atualizado_em
            )
            SELECT @academicoId, @ano, @mes, @dia, p.entrada, p.saida,
                   COALESCE(NULLIF(@descricaoFeriado, ''), ''),
                   @tipoDia, @descricaoFeriado, now()
            FROM app_pontos p
            WHERE p.academico_id = @academicoId AND p.data = @data
            ON CONFLICT (academico_id, ano, mes, dia) DO UPDATE
            SET entrada = EXCLUDED.entrada,
                saida = EXCLUDED.saida,
                tipo_dia = EXCLUDED.tipo_dia,
                descricao_feriado = EXCLUDED.descricao_feriado,
                atualizado_em = now()
            """, con);

        cmd.Parameters.AddWithValue("academicoId", academicoId);
        cmd.Parameters.AddWithValue("ano", data.Year);
        cmd.Parameters.AddWithValue("mes", data.Month);
        cmd.Parameters.AddWithValue("dia", data.Day);
        cmd.Parameters.AddWithValue("data", data.Date);
        cmd.Parameters.AddWithValue("tipoDia", feriado.TipoDia);
        cmd.Parameters.AddWithValue("descricaoFeriado", feriado.Descricao);
        await cmd.ExecuteNonQueryAsync();
    }

    public static async Task SalvarObservacao(NpgsqlConnection con, int academicoId, DateTime data, string observacao, int? supervisorId)
    {
